package Aprendiendo.Metodos;

public class D_MayusculaMinuscula {
    public static void main(String[] args) {
        String txt = "hola";
        System.out.println(txt.toUpperCase()); // toUpperCase -> Mayúscula

        txt = "MaYor";
        System.out.println(txt.toLowerCase()); // toLowerCase -> Minúscula
    }
}
